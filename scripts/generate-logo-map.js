#!/usr/bin/env node

/**
 * Generates a mapping of company names to their actual logo filenames
 */

import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const LOGOS_DIR = path.join(__dirname, '..', 'public', 'logos');
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'utils', 'logoMap.ts');

async function generateLogoMap() {
  console.log('📊 Scanning logos directory...\n');

  // Get all logo files
  const files = await fs.readdir(LOGOS_DIR);
  const logoFiles = files.filter(f =>
    /\.(png|webp|avif|svg|jpg|jpeg)$/i.test(f)
  );

  console.log(`Found ${logoFiles.length} logo files\n`);

  // Create mapping object
  const mapping = {};

  for (const file of logoFiles) {
    const basename = file.replace(/\.(png|webp|avif|svg|jpg|jpeg)$/i, '');

    // Generate possible company names from the filename
    const variations = [
      basename, // exact match
      basename.replace(/_/g, ' '), // underscores to spaces
      basename.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase()), // Title Case
    ];

    // Add each variation to the mapping
    for (const variation of variations) {
      // Normalize the key (lowercase, no extra spaces)
      const key = variation.toLowerCase().trim();

      // Only set if not already set (prioritize first match)
      if (!mapping[key]) {
        mapping[key] = file;
      }
    }

    // Also add a version with all spaces removed (for names like "Woof Together" -> "wooftogether")
    const noSpacesKey = basename.replace(/_/g, '').toLowerCase();
    if (!mapping[noSpacesKey]) {
      mapping[noSpacesKey] = file;
    }
  }

  // Special cases
  mapping['mi:rna'] = mapping['mirna'] || 'MIRNA.png';

  // Generate TypeScript file
  const tsContent = `/**
 * Auto-generated mapping of company names to logo filenames
 * Generated by scripts/generate-logo-map.js
 *
 * DO NOT EDIT MANUALLY - Run 'npm run generate-logo-map' to regenerate
 */

export const logoMap: Record<string, string> = ${JSON.stringify(mapping, null, 2)};
`;

  await fs.writeFile(OUTPUT_FILE, tsContent, 'utf-8');

  console.log(`✅ Generated logo map with ${Object.keys(mapping).length} entries`);
  console.log(`📝 Saved to ${OUTPUT_FILE}\n`);
}

generateLogoMap().catch(console.error);
